// Nilai Awal Negatif
let angka = -5;
document.writeln(angka); // Output: -5 (berupa number)
document.writeln("</br>");

// Menambahkan nilai
angka++; // mengubah menjadi -4
// -5  (+1) = maka akan menjadi -4
document.writeln(angka); // Output: -4 (berupa number)
document.writeln("</br>");

// Mengubah nilai positif menjadi negatif
angka--; // mengubah menjadi -5
// -4 (-1) = maka akan menjadi -5
document.writeln(angka); // Output: -5 (berupa number)
document.writeln("</br>");

// Mengubah nilai Negatif menjadi positif
angka = -angka; // mengubah menjadi 5
// -5 - (-5) = maka akan menjadi positif 5
document.writeln(angka); // Output: 5 (berupa number)
document.writeln("</br>");
