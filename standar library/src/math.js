// Math merupakan class di JavaScript yang berisikan static property dan method untuk operasi matematika
// Ada banyak sekali static property dan method di Math
// Math hanya bisa digunakan untuk tipe data Number, tidak bisa digunakan untuk tipe data BigInt
// https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/Math
console.info(Math.max(10, 42, 12, 5320, 322, 3333, 234));
console.info(Math.min(10, 42, 12, 5320, 322, 3333, 234));
console.info(Math.round(10.5)); // dibulatkan ke atas hasilnya 11
console.info(Math.round(10.3)); // dibulatkan ke bawah hasilnya 10
console.info(Math.floor(10.8)); // selalu di bulatkan ke bawah
console.info(Math.ceil(10.2)); // selalu di bulatkan ke atas
